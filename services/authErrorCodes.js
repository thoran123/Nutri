  VALIDATION_ERROR: 'AUTH_VALIDATION_ERROR',
  INTERNAL_ERROR: 'AUTH_INTERNAL_ERROR',
  RATE_LIMITED: 'AUTH_RATE_LIMITED',
  NOT_FOUND: 'AUTH_NOT_FOUND',

  // Login
  MISSING_FIELDS: 'AUTH_MISSING_FIELDS',
  INVALID_CREDENTIALS: 'AUTH_INVALID_CREDENTIALS',
  ACCOUNT_LOCKED: 'AUTH_ACCOUNT_LOCKED',
  ACCOUNT_DISABLED: 'AUTH_ACCOUNT_DISABLED',

  // MFA
  MFA_REQUIRED: 'AUTH_MFA_REQUIRED',
  MFA_INVALID: 'AUTH_MFA_INVALID',
  MFA_DISABLED: 'AUTH_MFA_DISABLED',
  MFA_RESEND_FAILED: 'AUTH_MFA_RESEND_FAILED',
  MFA_CHALLENGE_PENDING: 'AUTH_MFA_CHALLENGE_PENDING',

  // Token lifecycle
  TOKEN_INVALID: 'AUTH_TOKEN_INVALID',
  TOKEN_EXPIRED: 'AUTH_TOKEN_EXPIRED',
  REFRESH_FAILED: 'AUTH_REFRESH_FAILED',
  LOGOUT_FAILED: 'AUTH_LOGOUT_FAILED',

  // Password change (authenticated user)
