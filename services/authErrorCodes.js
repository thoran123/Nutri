  [AUTH_ERROR_CODES.VALIDATION_ERROR]: 400,
  [AUTH_ERROR_CODES.INTERNAL_ERROR]: 500,
  [AUTH_ERROR_CODES.RATE_LIMITED]: 429,
  [AUTH_ERROR_CODES.NOT_FOUND]: 404,

  [AUTH_ERROR_CODES.MISSING_FIELDS]: 400,
  [AUTH_ERROR_CODES.INVALID_CREDENTIALS]: 401,
  [AUTH_ERROR_CODES.ACCOUNT_LOCKED]: 423,
  [AUTH_ERROR_CODES.ACCOUNT_DISABLED]: 403,

  [AUTH_ERROR_CODES.MFA_REQUIRED]: 401,
  [AUTH_ERROR_CODES.MFA_INVALID]: 401,
  [AUTH_ERROR_CODES.MFA_DISABLED]: 404,
