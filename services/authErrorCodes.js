  [AUTH_ERROR_CODES.MFA_CHALLENGE_PENDING]: 202,

  [AUTH_ERROR_CODES.TOKEN_INVALID]: 401,
  [AUTH_ERROR_CODES.TOKEN_EXPIRED]: 401,
  [AUTH_ERROR_CODES.REFRESH_FAILED]: 401,
  [AUTH_ERROR_CODES.LOGOUT_FAILED]: 500,

  [AUTH_ERROR_CODES.CURRENT_PASSWORD_REQUIRED]: 400,
  [AUTH_ERROR_CODES.CURRENT_PASSWORD_INVALID]: 401,
  [AUTH_ERROR_CODES.NEW_PASSWORD_REQUIRED]: 400,
