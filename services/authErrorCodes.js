/**
 * services/authErrorCodes.js
 *
 * Canonical machine-readable error codes for the authentication lifecycle.
 *
 * Web and mobile clients should branch on these codes (NOT on HTTP status
 * alone, NOT on free-text error messages). Codes are stable and additive:
 * existing values must not change semantics; new flows append new codes.
 */

